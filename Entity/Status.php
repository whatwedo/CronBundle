namespace whatwedo\CronBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Bridge\Doctrine\Validator\Constraints\UniqueEntity;

/**
 * Class Execution
 *
 * @ORM\Table(name="whatwedo_cron_status")
 * @ORM\Entity(repositoryClass="whatwedo\CronBundle\Repository\StatusRepository")
 * @UniqueEntity("class")
 */
class Status
{
    /**
     * @var int|null
     *
     * @ORM\Column(name="id", type="bigint", nullable=false, options={"unsigned"=true})
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @var string
     *
     * @ORM\Column(name="class", type="string", length=255, nullable=false, unique=true)
     */
    protected $class;

    /**
     * @ORM\Column(name="active", type="boolean", nullable=false)
     *
     * @var bool
     */
    protected $active;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getClass(): string
    {
        return $this->class;
    }

    public function setClass(string $class): self
    {
        $this->class = $class;
        return $this;
    }

    public function getActive(): bool
    {
        return $this->active;
    }

    public function setActive(bool $active): self
    {
        $this->active = $active;
        return $this;
    }
}
